using System.Diagnostics;
using System.Diagnostics.CodeAnalysis;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.Testing;
using Microsoft.CodeAnalysis.Text;

namespace Bitwarden.Server.Sdk.Features.Analyzers.Tests;

public class FeaturesGeneratorTests
{
    [Fact]
    public async Task NoNamespace_Works()
    {
        await RunAsync(
            """
            using Bitwarden.Server.Sdk.Features;

            [FlagKeyCollection]
            public class MyFlags
            {
                public const string Flag = "my-flag";
            }
            """,
            """
            // <auto-generated/>
            #nullable enable

            using System.Collections.Generic;

            partial class MyFlags
            {
                public static IReadOnlyCollection<string> GetKeys()
                {
                    return [
                        Flag,
                    ];
                }
            }

            """
        );
    }

    [Fact]
    public async Task WithNamespace_Works()
    {
        await RunAsync(
            """
            using Bitwarden.Server.Sdk.Features;

            namespace MyApp
            {
                [FlagKeyCollection]
                public class MyFlags
                {
                    public const string Flag = "my-flag";
                }
            }
            """,
            """
            // <auto-generated/>
            #nullable enable

