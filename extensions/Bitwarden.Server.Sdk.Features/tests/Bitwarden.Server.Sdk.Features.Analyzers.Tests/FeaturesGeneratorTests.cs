                            ConstFlag,
                        ];
                    }
                }
            }
            """
        );
    }

    private static readonly CSharpParseOptions _parseOptions = new(LanguageVersion.CSharp12);

    private async Task RunAsync([StringSyntax("C#")] string source, [StringSyntax("C#")] string generatedSource)
    {
        var projectName = $"FeaturesGeneratorTestProject-{Guid.NewGuid()}";
        var compilationOptions = new CSharpCompilationOptions(OutputKind.DynamicallyLinkedLibrary)
            .WithNullableContextOptions(NullableContextOptions.Enable);

        var project = new AdhocWorkspace().CurrentSolution
            .AddProject(projectName, projectName, LanguageNames.CSharp)
            .WithCompilationOptions(compilationOptions)
            .WithParseOptions(_parseOptions);

        var dotNetDir = Path.GetDirectoryName(typeof(object).Assembly.Location);

        project = project
            .AddMetadataReference(MetadataReference.CreateFromFile(typeof(IFeatureService).Assembly.Location))
            .AddMetadataReference(MetadataReference.CreateFromFile(typeof(object).Assembly.Location))
            .AddMetadataReference(MetadataReference.CreateFromFile(Path.Join(dotNetDir, "System.Runtime.dll")));

        project = project
            .AddDocument("TestSource.cs", SourceText.From(source, Encoding.UTF8))
            .Project;

        var initialCompilation = await project.GetCompilationAsync(TestContext.Current.CancellationToken);

        if (initialCompilation is null)
        {
            Assert.Fail("Could not compile test project");
        }

        var initialDiagnostics = initialCompilation.GetDiagnostics(TestContext.Current.CancellationToken);

        if (initialDiagnostics.Length > 0)
        {
            Debug.WriteLine("Initial diagnostics:");
            foreach (var diagnostic in initialDiagnostics)
            {
                Debug.WriteLine(diagnostic.ToString());
            }
        }

        var generator = new FeaturesGenerator().AsSourceGenerator();
        GeneratorDriver driver = CSharpGeneratorDriver.Create(
            generators: [generator],
            driverOptions: new GeneratorDriverOptions(IncrementalGeneratorOutputKind.None, trackIncrementalGeneratorSteps: true),
            parseOptions: _parseOptions
        );

        driver = driver.RunGeneratorsAndUpdateCompilation(initialCompilation, out var updatedCompilation, out var updatedDiagnostics, TestContext.Current.CancellationToken);

        if (updatedDiagnostics.Length > 0)
        {
            Assert.Fail("Updated compilation has diagnostics.");
        }

        var runResult = driver.GetRunResult();

        var singleResult = Assert.Single(runResult.Results);

        var singleGeneratedSource = Assert.Single(singleResult.GeneratedSources);

        new DefaultVerifier().EqualOrDiff(generatedSource, singleGeneratedSource.SourceText.ToString());
    }
}
